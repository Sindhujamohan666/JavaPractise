package com.thetestingacademy.String.StringReverse;

public class StringReverse {
    public static void main(String[] args) {
        String[] s={"Jovika","Poovika","Malavika"};
        //looping
        for(int i=s.length-1;i>=0;i--){
            System.out.print (s[i]);
            System.out.println(" ");

        }

    }
}
